var d = new Date();


// getDate()
console.log("======= Date ========");
console.log(d.getDate());


// getDay()
console.log("======= Day ========");
console.log(d.getDay());


// getFullYear()
console.log("======= Full Year ========");
console.log(d.getFullYear());


// getMonth()
console.log("======= Month ========");
console.log(d.getMonth());


// getToDateString
console.log("======= Date In String ========");
console.log(d.toDateString());


// setDate()
console.log("======= setDate ========");
console.log(d.setDate(31));
console.log(d);



// setFullYear()
console.log("======= setFullYear ========");
console.log(d.setFullYear(2021,0,01));
